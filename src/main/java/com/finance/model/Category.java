package com.finance.model;

/**
 * Represents common spending and income categories.
 */
public enum Category {
    /** Food and groceries. */
    FOOD,
    /** Travel and commuting expenses. */
    TRANSPORT,
    /** Salary or other income. */
    SALARY,
    /** Leisure and entertainment. */
    ENTERTAINMENT,
    /** Bills and recurring household costs. */
    UTILITIES,
    /** Any uncategorized transaction. */
    OTHER;
}
